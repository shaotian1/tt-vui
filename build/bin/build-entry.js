const fs = require('fs');
const { resolve } = require('path');
const render = require('json-templater/string');
const endOfLine = require('os').EOL;
const components = require('../../components.json');

const OUTPUT_PATH = resolve(__dirname, '../../src/index.js');
const IMPORT_TPL = 'import {{name}} from "src/packages/{{package}}/index.js";';
const INSTALL_COMPONENT_TEMPLATE = '  {{name}}';
const MAIN_TEMPLATE = `/* Automatically generated by 'build/bin/build-entry.js' */
{{include}}

const components = [
{{install}},
];

const install = (Vue) => {
  components.forEach(component => {
    Vue.component(component.name, component);
  });
};

if (typeof window !== 'undefined' && window.Vue) {
  install(window.Vue);
}

export default {
  version: '{{version}}',
  install,
{{list}}
};
`;

const componentNames = Object.keys(components);

const includeComponentTemplate = [];
const installTemplate = [];
const listTemplate = [];

componentNames.forEach(name => {
  includeComponentTemplate.push(
    render(IMPORT_TPL, {
      name,
      package: name,
    })
  );

  installTemplate.push(
    render(INSTALL_COMPONENT_TEMPLATE, {
      name,
    })
  );

  listTemplate.push(`  ${name}`);
});

const template = render(MAIN_TEMPLATE, {
  include: includeComponentTemplate.join(endOfLine),
  install: installTemplate.join(',' + endOfLine),
  version: require('../../package.json').version,
  list: listTemplate.join(',' + endOfLine),
});

fs.writeFileSync(OUTPUT_PATH, template);
console.log('[build entry] DONE:', OUTPUT_PATH);
